Bitboard
square_a1h8_diagonal(Square sq) {
	return DIAGONALS_A1H8[7 + square_rank(sq) - square_file(sq)];
}

Bitboard
square_a8h1_diagonal(Square sq) {
	return DIAGONALS_A8H1[square_rank(sq) - square_file(sq)];
}
